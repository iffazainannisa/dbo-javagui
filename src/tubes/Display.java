    private void jButtonTentukanRapatRapatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonTentukanRapatRapatActionPerformed
        Integer maxCount;
        String  maxValue;
        maxValue = rapat.getElementAt(0);
        maxCount = 1;
        for (int i = 0; i < rapat.getSize(); ++i) {
            Integer count = 0;
                for (int j = 1; j < rapat.getSize(); ++j) {
                    if (rapat.getElementAt(j) == rapat.getElementAt(i)) ++count;
                }
            if (count > maxCount) {
                maxCount = count;
                maxValue = rapat.getElementAt(i);
            }
        }
        jRajinRapat.setText(maxValue);
    }//GEN-LAST:event_jButtonTentukanRapatRapatActionPerformed

    private void jButtonTentukanPiketActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonTentukanPiketActionPerformed
        Integer maxcount;
        String maxvalue;
        maxvalue = piket.getElementAt(1);
        maxcount = 1;
        for (int i = 0; i < piket.getSize(); i++) {
            Integer count = 0;
                for (int j = 1; j < piket.getSize(); j++) {
                    if (piket.getElementAt(j) == piket.getElementAt(i)) count++;
                }
            if (count > maxcount) {
                maxcount = count;
                maxvalue = piket.getElementAt(i);
            }
        }
        jRajinPiket.setText(maxvalue);
    }//GEN-LAST:event_jButtonTentukanPiketActionPerformed

